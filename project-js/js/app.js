const SearchInput = document.querySelector('#search-item');
const allProduct = document.querySelector('.product-list');

function Output(Product){
    let html = '';
   Product.forEach(Products => {
    html +=`
    <div class="col-lg-3 col-12 box">
    <div class="card mb-3">
        <img class "card-img-top rounded-3 " src="${Products.img}" alt="Card image cap ">
        <div class = "card-body mb-3">
        <h5 class = "card-title">${Products.name}</h5>
        <p class = "card-text">${Products.description}</p>
        <p class = "card-text price">Price : $${Products.price}</p>
        <a href="#" class="add">Add to Card</a>
        </div>
    </div>
</div>
    `;
   });
   return html;
}
function updateProductlist(Product){
    allProduct.innerHTML = Output(Product);
}

// Intialize Render function
updateProductlist(Product);

// Event Listeners for Search Input
SearchInput.addEventListener('input', function (){
    const SearchTerm = SearchInput.value.toLowerCase();
    const FilterProduct = Product.filter(Products =>
        Products.name.toLowerCase().includes(SearchTerm)
    );
    updateProductlist(FilterProduct);
});

