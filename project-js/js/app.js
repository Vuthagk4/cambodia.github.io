const allProducts = document.querySelector('.product-list');


function renderProducts(){
    let html = '';
    // Use ForEach to loop through all products array
    Product.forEach(Product => {
        html = html + `
    <div class="col-lg-3 col-12 box">
        <div class="card mb-3">
            <img class "card-img-top rounded-3 " src="${Product.img}" alt="Card image cap ">
            <div class = "card-body mb-3">
            <h5 class = "card-title">${Product.name}</h5>
            <p class = "card-text">${Product.description}</p>
            <p class = "card-text price">Price : $${Product.price}</p>
            <a href="#" class="btn btn-outline-primary">Add to Card</a>
            </div>
        </div>
    </div>
        `;
    });
    return html;
}
allProducts.innerHTML = renderProducts();

// scroll we change navbar background color
window.addEventListener('scroll', function(){
    const  navbar = document.querySelector('.navbar');
    if(window.scrollY > 0){
        navbar.classList.add('fixed-top');
    }
    else{
        navbar.classList.remove('fixed-top');
    }
});

document.addEventListener('DOMContentLoaded', function(){

    const moonIcon = document.querySelector('.moon');
    const navbar = document.querySelector(".navbar");
    const body = document.body;

    moonIcon.addEventListener('click', function(){
        body.classList.toggle('dark-mode');
        navbar.classList.toggle('dark-mode');
        moonIcon.classList.toggle('bi-sun');
    });
});

